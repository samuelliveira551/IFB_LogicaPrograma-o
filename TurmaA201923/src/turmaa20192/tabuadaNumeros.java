// Exercicio 10-Exercicio 2-Tabuada com todos os numeros 


package turmaa20192;

public class tabuadaNumeros {
	public static void main(String[] args) {

	for (int z=1; z<=10;z++){
		System.out.println("Tabuada de  "+z);
		
		for(int i = 1; i <= 10; i++) {
		System.out.println(i+" x "+z+" = "+(i*z));
		
		}
		System.out.println("\n");
		
	}
}
}